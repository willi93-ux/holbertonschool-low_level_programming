#include <stdlib.h>
#include <time.h>
#include <stdio.h>

/**
 * main - Entry point
 *
 * Description: Prints the alphabet in lowercase followed by a new line.
 * The program uses putchar to print each character.
 *
 * Return: Always 0 (Success)
 */
int main(void)
{
for (char w = 'a'; w <= 'z'; w++)
  putchar(w);
return (0);
}



